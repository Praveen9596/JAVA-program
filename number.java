public class number{ 
 public static void main(String[] args) {
    for(int i=0;i<6;i++)
	{
		int counter = 0;
		while(i>counter)
		{
		counter = counter +1;
		System.out.print(counter);  	
		}
		System.out.println();  	
	}
	for(int i=4;i>0;i--)
	{
		int counter = 0;
		while(i>counter)
		{
		counter = counter +1;
		System.out.print(counter);  	
		}
		System.out.println();  	
	}	
  }
}
